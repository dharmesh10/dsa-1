package treesandgraphs.structure;

public class TreeNode {
  public int val;
  public TreeNode left;
  public TreeNode right;

  public TreeNode(int val) {
    this.val = val;
    left = null;
    right = null;
  }

  public void setLeft(TreeNode node) {
    this.left = node;
  }

  public void setRight(TreeNode node) {
    this.right = right;
  }
}
